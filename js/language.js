// ==== Aplica traducciones a todos los elementos con data-i18n dentro de un root dado ====
function applyTranslations(lang, root = document) {
  root.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.innerText = translations[lang][key];
    }
  });
}

// ==== Guarda el idioma en localStorage y re-aplica traducciÃ³n en todo el documento ====
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('language', lang);
  applyTranslations(lang);
}

// ==== Cuando el DOM estÃ© listo ====
document.addEventListener('DOMContentLoaded', () => {
  // 1) Aplicar traducciÃ³n inicial al contenido ya existente
  applyTranslations(currentLang);

  // 2) Montar el MutationObserver para detectar nuevos nodos con data-i18n
  const observer = new MutationObserver(mutations => {
    mutations.forEach(mutation => {
      mutation.addedNodes.forEach(node => {
        if (node.nodeType !== Node.ELEMENT_NODE) return;
        // Traducir el nodo agregado (si mismo y sus hijos que tengan data-i18n)
        applyTranslations(currentLang, node);
      });
    });
  });

  observer.observe(document.body, {
    childList: true,
    subtree: true
  });

  // 3) Asignar eventos a los botones de cambio de idioma
  function attachLanguageButtons() {
    const buttons = document.querySelectorAll('.language-option');
    if (buttons.length === 0) {
      // Si aÃºn no existe el dropdown (header no inyectado), reintentar en 50ms
      setTimeout(attachLanguageButtons, 50);
      return;
    }
    buttons.forEach(btn => {
      btn.addEventListener('click', () => {
        const lang = btn.getAttribute('data-lang');
        setLanguage(lang);
      });
    });
  }

  attachLanguageButtons();
});